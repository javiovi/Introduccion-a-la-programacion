public class numeroWhile {
    public static void main(String[] args) {
        int numeroWhile = 3;

        while (numeroWhile < 3) {
            System.out.println(numeroWhile);
            numeroWhile++;
        }
        do {
            System.out.println(numeroWhile);
            numeroWhile++;
        } while (numeroWhile < 3);

        }
}
